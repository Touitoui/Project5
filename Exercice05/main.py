def sum(a, b):
    """
    Calcule la somme de deux nombres.

    Args:
        a: Premier nombre à additionner
        b: Second nombre à additionner

    Returns:
        La somme de a et b
    """
    return a + b


def subtraction(a, b):
    """
    Calcule la différence entre deux nombres.

    Args:
        a: Le nombre duquel on soustrait
        b: Le nombre à soustraire

    Returns:
        La différence entre a et b
    """
    return a - b
